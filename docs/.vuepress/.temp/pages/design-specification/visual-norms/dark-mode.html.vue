<template><h1 id="暗黑模式" tabindex="-1"><a class="header-anchor" href="#暗黑模式" aria-hidden="true">#</a> 暗黑模式</h1>
<p>暗黑模式是指把所有 UI 换成黑色或者深色的一个主题模式。</p>
<h2 id="何时使用" tabindex="-1"><a class="header-anchor" href="#何时使用" aria-hidden="true">#</a> 何时使用</h2>
<ul>
<li>
<p>当需要长时间在暗光环境下工作时，建议使用暗黑模式，减小亮度对比带来的视觉压力，保证使用者的体验舒适。</p>
</li>
<li>
<p>当需要沉浸式的专注工作内容时，建议使用暗黑模式，可以帮助使用者更关注凸显出来的内容和操作。</p>
</li>
</ul>
<blockquote>
<p>如同在电影院看电影时要全场关灯，是一样的原理。</p>
</blockquote>
<h2 id="设计原则" tabindex="-1"><a class="header-anchor" href="#设计原则" aria-hidden="true">#</a> 设计原则</h2>
<ul>
<li>内容的舒适性</li>
</ul>
<p>暗黑模式下避免使用对比很强的色彩或内容，长时间使用会带来疲劳感。</p>
<ul>
<li>信息的一致性</li>
</ul>
<p>暗黑模式下的信息内容需要和浅色模式保持一致性，不应该打破原有的层级关系。</p>
